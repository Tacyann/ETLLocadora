package br.com.model;

public class Artistas {
	
	private Integer cod_art;
	private String tpo_art;
	private String nac_bras;
	private Integer cod_grav;
	private Integer qtd_tit;
	private Integer med_anual;
	private String nom_art;

	
	public Integer getCod_art() {
		return cod_art;
	}
	public void setCod_art(Integer cod_art) {
		this.cod_art = cod_art;
	}
	public String getTpo_art() {
		return tpo_art;
	}
	public void setTpo_art(String tpo_art) {
		this.tpo_art = tpo_art;
	}
	public String getNac_bras() {
		return nac_bras;
	}
	public void setNac_bras(String nac_bras) {
		this.nac_bras = nac_bras;
	}
	public Integer getCod_grav() {
		return cod_grav;
	}
	public void setCod_grav(Integer cod_grav) {
		this.cod_grav = cod_grav;
	}
	public Integer getQtd_tit() {
		return qtd_tit;
	}
	public void setQtd_tit(Integer qtd_tit) {
		this.qtd_tit = qtd_tit;
	}
	public Integer getMed_anual() {
		return med_anual;
	}
	public void setMed_anual(Integer med_anual) {
		this.med_anual = med_anual;
	}
	public String getNom_art() {
		return nom_art;
	}
	public void setNom_art(String nom_art) {
		this.nom_art = nom_art;
	}

}
