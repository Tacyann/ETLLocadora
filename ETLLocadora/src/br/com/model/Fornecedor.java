package br.com.model;

public class Fornecedor {
	private Integer cod_forn;
	private String uf_forn;
	private Integer sld_credor;
	private String nom_forn;
	public Integer getCod_forn() {
		return cod_forn;
	}
	public void setCod_forn(Integer cod_forn) {
		this.cod_forn = cod_forn;
	}
	public String getUf_forn() {
		return uf_forn;
	}
	public void setUf_forn(String uf_forn) {
		this.uf_forn = uf_forn;
	}
	public Integer getSld_credor() {
		return sld_credor;
	}
	public void setSld_credor(Integer sld_credor) {
		this.sld_credor = sld_credor;
	}
	public String getNom_forn() {
		return nom_forn;
	}
	public void setNom_forn(String nom_forn) {
		this.nom_forn = nom_forn;
	}
	
	
}
