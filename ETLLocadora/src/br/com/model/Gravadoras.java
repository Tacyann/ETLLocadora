package br.com.model;

public class Gravadoras {
	
	private Integer cod_grav;
	private String uf_grav;
	private String nac_bras;
	private String nom_grav;
	public Integer getCod_grav() {
		return cod_grav;
	}
	public void setCod_grav(Integer cod_grav) {
		this.cod_grav = cod_grav;
	}
	public String getUf_grav() {
		return uf_grav;
	}
	public void setUf_grav(String uf_grav) {
		this.uf_grav = uf_grav;
	}
	public String getNac_bras() {
		return nac_bras;
	}
	public void setNac_bras(String nac_bras) {
		this.nac_bras = nac_bras;
	}
	public String getNom_grav() {
		return nom_grav;
	}
	public void setNom_grav(String nom_grav) {
		this.nom_grav = nom_grav;
	}
	
	

}
